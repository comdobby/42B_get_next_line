#include "get_next_line.h"

t_list	*ft_lstlast(t_list *head)
{
	t_list	*node;

	node = head;
	while (node && node->next)
		node = node->next;
	return (node);
}

size_t	ft_strlen(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}
